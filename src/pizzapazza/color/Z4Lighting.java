package pizzapazza.color;

/**
 * The lighting of a color
 *
 * @author gianpiero.diblasi
 */
public enum Z4Lighting {

  /**
   * No lighting
   */
  NONE,
  /**
   * lighting from in to out
   */
  LIGHTED_IN_OUT,
  /**
   * darkening from in to out
   */
  DARKENED_IN_OUT,
  /**
   * lighting from out to in
   */
  LIGHTED_OUT_IN,
  /**
   * darkening from out to in
   */
  DARKENED_OUT_IN;
}
