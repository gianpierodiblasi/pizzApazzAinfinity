package pizzapazza.math;

/**
 * The point
 *
 * @author gianpiero.diblasi
 */
public class Z4Point {

  public final double x;
  public final double y;

  /**
   * Creates the object
   *
   * @param x The x-axis coordinate
   * @param y The y-axis coordinate
   */
  public Z4Point(double x, double y) {
    this.x = x;
    this.y = y;
  }
}
