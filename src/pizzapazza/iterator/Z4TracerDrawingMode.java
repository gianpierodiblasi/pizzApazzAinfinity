/**
 * The drawing mode of a Z4Tracer
 *
 * @author gianpiero.diblasi
 */
public enum Z4TracerDrawingMode {

  /**
   * The free drawing
   */
  FREE,
  /**
   * The assisted drawing
   */
  ASSISTED,
  /**
   * The ruler mode
   */
  RULER,
  /**
   * The shapes &amp; paths mode
   */
  SHAPES_AND_PATHS;
}
