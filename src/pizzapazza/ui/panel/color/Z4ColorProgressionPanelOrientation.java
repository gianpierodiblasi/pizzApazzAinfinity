package pizzapazza.ui.panel.color;

/**
 * The orientation of a color progression panel
 *
 * @author gianpiero.diblasi
 */
public enum Z4ColorProgressionPanelOrientation {
  /**
   * The color progression panel is visualized horizontally with a
   * compact width
   */
  HORIZONTALLY_COMPACT,
  /**
   * The color progression panel is visualized horizontally with a
   * compact height
   */
  VERTICALLY_COMPACT;
}
