package pizzapazza.ui.panel.color;

/**
 * The orientation of a (bi)gradient color progression panel
 *
 * @author gianpiero.diblasi
 */
public enum Z4GradientColorProgressionPanelOrientation {
  /**
   * The gradient color progression panel is visualized horizontally with a
   * compact width
   */
  HORIZONTALLY_COMPACT,
  /**
   * The gradient color progression panel is visualized horizontally with a
   * compact height
   */
  VERTICALLY_COMPACT;
}
